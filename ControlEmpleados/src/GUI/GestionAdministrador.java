    public adminBL RecolectarDato(){
        try{
            objadminBL.setId(Integer.valueOf(txtID.getText()));
        }catch(Exception e){
            System.out.println("ERROR: ID");
        }
        objadminBL.setUsuario(txtUsuario.getText());
        objadminBL.setPassword(txtPassword.getText());
        if(rbtnGeneral.isSelected() == true)
        {
            objadminBL.setTipo(rbtnGeneral.getText());
        }
        else{
            objadminBL.setTipo(rbtnContador.getText());
        }
        return objadminBL;
    }
    
    public void limpiarFormulario(){
    
        txtID.setText(null);
        txtUsuario.setText(null);
        txtPassword.setText(null);
    }
    
