    private void tblAdminMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tblAdminMouseClicked
        // TODO add your handling code here:
        int row = tblAdmin.getSelectedRow();
        JTable target = (JTable)evt.getSource();
        txtID.setText(target.getValueAt(row, 0).toString());
        txtUsuario.setText(target.getValueAt(row, 1).toString());
        txtPassword.setText(target.getValueAt(row, 2).toString());
        String tipo = target.getValueAt(row, 3).toString();
        if(tipo.equals("Admin.General")){
        
            rbtnGeneral.setSelected(true);
        }
        else{
        
            rbtnContador.setSelected(true);
        }
        
    }//GEN-LAST:event_tblAdminMouseClicked

